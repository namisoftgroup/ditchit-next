export function buildLocaleCountryPath(locale: string, country: string, path = "") {
  return `/${locale}-${country}${path.startsWith("/") ? path : `/${path}`}`;
}

export function parseLocaleCountry(pathname: string) {
  const match = pathname.match(/^\/([a-z]{2,3})(?:-([A-Z]{2}))?(\/.*)?$/);
  if (!match) return { locale: "en", country: undefined, restPath: pathname };

  const [, locale, country, restPath = "/"] = match;
  return { locale, country, restPath };
}
